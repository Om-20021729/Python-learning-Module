name = "Rudra"

print(name[0:3])
print(name[-4:-1])
print(name[1:4]) # in negative slicing printing order is not changed
print(name[:4]) # when number is not given then assuming zero in semicolon is same as print(name[0:4])
print(name[2:]) # when number is not give in this type so assume last number  is same as print(name[2:5])